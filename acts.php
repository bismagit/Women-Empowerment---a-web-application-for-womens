  <h3><b>CONSTITUTION OF INDIA AND WOMEN EMPOWERMENT</b></h3><br/>
     	India’s Constitution makers and our founding fathers were very determined to provide equal rights to both women and men. The Constitution of India is one of the finest equality documents in the world. It provides provisions to secure equality in general and gender equality in particular. Various articles in the Constitution safeguard women’s rights by putting them at par with men socially, politically and economically.<br/><br/>

The Preamble, the Fundamental Rights, DPSPs and other constitutional provisions provide several general and special safeguards to secure women’s human rights.<br/><br/>
<b>Preamble:</b><br/>
The Preamble to the Constitution of India assures justice, social, economic and political; equality of status and opportunity and dignity to the individual. Thus it treats both men and women equal.<br/><br/>

<b>Fundamental Rights:</b><br/>

The policy of women empowerment is well entrenched in the Fundamental Rights enshrined in our Constitution. For instance:<br/><br/>

Article 14 ensures to women the right to equality.<br/>
Article 15(1) specifically prohibits discrimination on the basis of sex.<br/>
Article 15(3) empowers the State to take affirmative actions in favour of women.<br/>
Article 16 provides for equality of opportunity for all citizens in matters relating to employment or appointment to any office.<br/><br/>
These rights being fundamental rights are justiciable in court and the Government is obliged to follow the same.<br/>
<br/>




<b>Directive Principles of State Policy:</b><br/>

Directive principles of State Policy also contains important provisions regarding women empowerment and it is the duty of the government to apply these principles while making laws or formulating any policy. Though these are not justiciable in the Court but these are essential for governance nonetheless. Some of them are:<br/><br/>

Article 39 (a) provides that the State to direct its policy towards securing for men and women equally the right to an adequate means of livelihood.<br/>
Article 39 (d) mandates equal pay for equal work for both men and women.<br/>
Article 42 provides that the State to make provision for securing just and humane conditions of work and for maternity relief.<br/><br/>

<b>Fundamental Duties:</b><br/>
Fundamental duties are enshrined in Part IV-A of the Constitution and are positive duties for the people of India to follow. It also contains a duty related to women’s rights:<br/><br/>

Article 51 (A) (e) expects from the citizen of the country to promote harmony and the spirit of common brotherhood amongst all the people of India and to renounce practices derogatory to the dignity of women.<br/>

<b>Other Constitutional Provisions:</b><br/>
Through 73rd and 74th Constitutional Amendment of 1993, a very important political right has been given to women which is a landmark in the direction of women empowerment in India. With this amendment women were given 33.33 percent reservation in seats at different levels of elections in local governance i.e. at Panchayat, Block and Municipality elections.<br/><br/>

Thus it can be seen that these Constitutional provisions are very empowering for women and the State is duty bound to apply these principles in taking policy decisions as well as in enacting laws.<br/><br/><br/>

<h3><b>SPECIFIC LAWS FOR WOMEN EMPOWERMENT IN INDIA</b></h3><br/>
Here is the list of some specific laws which were enacted by the Parliament in order to fulfil Constitutional obligation of women empowerment:<br/><br/>

The Equal Remuneration Act, 1976.<br/>
The Dowry Prohibition Act, 1961.<br/>
The Immoral Traffic (Prevention) Act, 1956.<br/>
The Maternity Benefit Act, 1961.<br/>
The Medical termination of Pregnancy Act, 1971.<br/>
The Commission of Sati (Prevention) Act, 1987.<br/>
The Prohibition of Child Marriage Act, 2006.<br/>
The Pre-Conception & Pre-Natal Diagnostic Techniques (Regulation and Prevention of Misuse) Act, 1994.<br/>
The Sexual Harassment of Women at Work Place (Prevention, Protection and) Act, 2013.
Above mentioned and several other laws are there which not only provide specific legal rights to women but also gives them a sense of security and empowerment.<br/><br/>

<h3><b>INTERNATIONAL COMMITMENTS OF INDIA AS TO WOMEN EMPOWERMENT</b></h3><br/>
India is a part to various International conventions and treaties which are committed to secure equal rights of women.<br/><br/>

One of the most important among them is the Convention on Elimination of All Forms of Discrimination against Women (CEDAW), ratified by India in 1993.<br/><br/>

Other important International instruments for women empowerment are: The Mexico Plan of Action (1975), the Nairobi Forward Looking Strategies (1985), the Beijing Declaration as well as the Platform for Action (1995) and the Outcome Document adopted by the UNGA Session on Gender Equality and Development & Peace for the 21st century, titled “Further actions and initiatives to implement the Beijing Declaration and the Platform for Action”. All these have been whole-heartedly endorsed by India for appropriate follow up.<br/><br/>

These various national and International commitments, laws and policies notwithstanding women’s situation on the ground have still not improved satisfactorily. Varied problems related to women are still subsisting; female infanticide is growing, dowry is still prevalent, domestic violence against women is practised; sexual harassment at workplace and other heinous sex crimes against women are on the rise.<br/><br/>

Though, economic and social condition of women has improved in a significant way but the change is especially visible only in metro cities or in urban areas; the situation is not much improved in semi-urban areas and villages. This disparity is due to lack of education and job opportunities and negative mind set of the society which does not approve girls’ education even in 21st century.<br/><br/>

<h3><b>GOVERNMENT POLICIES AND SCHEMES FOR WOMEN EMPOWERMENT</b></h3><br/>
Whatever improvement and empowerment women have received is especially due to their own efforts and struggle, though governmental schemes are also there to help them in their endeavour.<br/><br/>

In the year 2001, the Government of India launched a National Policy for Empowerment of Women. The specific objectives of the policy are as follows:<br/><br/>

Creation of an environment through positive economic and social policies for full development of women to enable them to realize their full potential.
Creation of an environment for enjoyments of all human rights and fundamental freedom by women on equal basis with men in all political, economic, social, cultural and civil spheres.<br/>
Providing equal access to participation and decision making of women in social political and economic life of the nation.<br/>
Providing equal access to women to health care, quality education at all levels, career and vocational guidance, employment, equal remuneration, occupational health and safety, social security and public life etc.<br/>
Strengthening legal systems aimed at elimination of all forms of discrimination against women.<br/>
Changing societal attitudes and community practices by active participation and involvement of both men and women.<br/><br/>
Mainstreaming a gender perspective in the development process.
Elimination of discrimination and all forms of violence against women and the girl child.
Building and strengthening partnerships with civil society, particularly women’s organizations.<br/><br/>
The Ministry of Women and Child Development is the nodal agency for all matters pertaining to welfare, development and empowerment of women. It has evolved schemes and programmes for their benefit. These schemes are spread across a very wide spectrum such as women’s need for shelter, security, safety, legal aid, justice, information, maternal health, food, nutrition etc., as well as their need for economic sustenance through skill development, education and access to credit and marketing.<br/><br/>

Various schemes of the Ministry are like Swashakti, Swayamsidha, STEP and Swawlamban enable economic empowerment. Working Women Hostels and Creches provide support services. Swadhar and Short Stay Homes provide protection and rehabilitation to women in difficult circumstances. The Ministry also supports autonomous bodies like National Commission, Central Social Welfare Board and Rashtriya Mahila Kosh which work for the welfare and development of women. Economic sustenance of women through skill development, education and access to credit and marketing is also one of the areas where the Ministry has special focus.<br/>

</b>Conclusion and Suggestions:</b><br/><br/>

In conclusion, it can be said that women in India, through their own unrelenting efforts and with the help of Constitutional and other legal provisions and also with the aid of Government’s various welfare schemes, are trying to find their own place under the sun. And it is a heartening sign that their participation in employment- government as well as private, in socio-political activities of the nation and also their presence at the highest decision making bodies is improving day by day.<br/><br/>

However, we are still far behind in achieving the equality and justice which the Preamble of our Constitution talks about. The real problem lies in the patriarchal and male-dominated system of our society which considers women as subordinate to men and creates different types of methods to subjugate them.<br/><br/>

The need of us is to educate and sensitize male members of the society regarding women issues and try to inculcate a feeling of togetherness and equality among them so that they would stop their discriminatory practices towards the fairer sex.<br/><br/>

For this to happen apart from Government, the efforts are needed from various NGOs and from enlightened citizens of the country. And first of all efforts should begin from our homes where we must empower female members of our family by providing them equal opportunities of education, health, nutrition and decision making without any discrimination.<br/><br/>

Because India can become a powerful nation only if it truly empowers its women.
