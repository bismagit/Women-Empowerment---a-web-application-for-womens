            <b> <h3>WHAT IS WOMEN EMPOWERMENT</h3></b><br/>

            Women empowerment in simple words can be understood as giving power to women to decide for their own lives or inculcating such abilities in them so that they could be able to find their rightful place in the society. <br/>

            According to the <b>United Nations</b>, women’s empowerment mainly has five components:<br/><br/>
            1) Generating women’s sense of self-worth;<br/>
            2) Women’s right to have and to determine their choices;<br/>
            3) Women’s right to have access to equal opportunities and all kinds of resources;<br/>
            4) Women’s right to have the power to regulate and control their own lives, within and outside the home; and<br/>
            5)Women’s ability to contribute in creating a more just social and economic order.<br/><br/>

            Thus, women empowerment is nothing but recognition of women’s basic human rights and creating an environment where they are treated as equals to men.<br/><br/>

             <h3><b>WOMEN EMPOWERMENT IN INDIA</b></h3><br/>
            <b>Historical Background:</b><br/>
            From ancient to modern period, women’s condition-socially, politically and economically- has not remained same and it kept changing with times. In ancient India, women were having equal status with men; in early Vedic period they were very educated and there are references of women sages such as Maitrayi in our ancient texts. But with the coming of famous treatise of Manu i.e. Manusmriti, the status of women was relegated to a subordinate position to men.

All kinds of discriminatory practices started to take from such as child marriage, devadashi pratha, nagar vadhu system, sati pratha etc. Women’s socio-political rights were curtailed and they were made fully dependent upon the male members of family. Their right to education, right to work and right to decide for themselves were taken away.
During medieval period the condition of women got worsened with the advent of Muslim rulers in India; as also during the British period. But the British rule also brought western ideas into the country.<br/><br/>

A few enlightened Indians such as Raja Ram Mohun Roy influenced by the modern concept of freedom, liberty, equality and justice started to question the prevailing discriminatory practices against women. Through his unrelenting efforts, the British were forced to abolish the ill-practice of Sati. Similarly several other social reformers such as Ishwar Chandra Vidyasagar, Swami Vivekananda, Acharya Vinoba Bhave etc. worked for the upliftment of women in India. For instance, the Widow Remarriage Act of 1856 was the result of Ishwar Chandra Vidyasagar’s movement for improving the conditions of widows.<br/><br/>

Indian National Congress supported the first women’s delegation which met the Secretary of State to demand women’s political rights in 1917. The Child Marriage Restraint Act in 1929 was passed due to the efforts of Mahhommad Ali Jinna, Mahatma Gandhi called upon the young men to marry the child widows and urged people to boycott child marriages.<br/><br/>
During freedom movement, almost all the leaders of the struggle were of the view that women should be given equal status in the free India and all types of discriminatory practices must stop. And for that to happen, it was thought fit to include such provisions in the Constitution of India which would help eliminate age-old exploitative customs and traditions and also such provisions which would help in empowering women socially, economically and politically.<br/><br/>
